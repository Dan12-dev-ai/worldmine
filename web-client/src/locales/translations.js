// Language detection and selection
export const detectLanguage = () => {
  // Get browser language
  const browserLang = navigator.language || navigator.userLanguage;
  
  // Extract primary language code
  const primaryLang = browserLang.split('-')[0].toLowerCase();
  
  // Check for specific language matches
  if (primaryLang === 'am' || browserLang.includes('amh')) {
    return 'am'; // Amharic
  } else if (primaryLang === 'es' || browserLang.includes('spa')) {
    return 'es'; // Spanish
  } else {
    return 'en'; // Default to English
  }
};

// Get translation function
export const getTranslation = (key, language = detectLanguage()) => {
  const keys = key.split('.');
  let translation = translations[language];
  
  for (const k of keys) {
    if (translation && translation[k]) {
      translation = translation[k];
    } else {
      // Fallback to English if translation not found
      translation = translations.en;
      for (const fallbackKey of keys) {
        if (translation && translation[fallbackKey]) {
          translation = translation[fallbackKey];
        } else {
          return key; // Return key if no translation found
        }
      }
      break;
    }
  }
  
  return translation;
};

// Hook for using translations
import React, { useState, useEffect } from 'react';

export const useTranslation = () => {
  const [language, setLanguage] = useState(detectLanguage());
  
  const t = (key) => getTranslation(key, language);
  
  const changeLanguage = (newLanguage) => {
    setLanguage(newLanguage);
    localStorage.setItem('worldmine-language', newLanguage);
  };
  
  useEffect(() => {
    // Check for saved language preference
    const savedLanguage = localStorage.getItem('worldmine-language');
    if (savedLanguage && translations[savedLanguage]) {
      setLanguage(savedLanguage);
    }
  }, []);
  
  return { t, language, changeLanguage, availableLanguages: ['en', 'am', 'es'] };
};
